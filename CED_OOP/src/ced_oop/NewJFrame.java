private  void listFiles(String path)
    {
        File folder = new File(path);
         
        File[] files = folder.listFiles();
       int x=0,y=0,z=0;
        for (File file : files)
        {
            if (file.isFile())
            { 
                 
                String g=file.getName();
                String h=file.getPath();

                if(g.endsWith(".xlsx")||g.endsWith(".xls"))
                {
                  jTable1.getModel().setValueAt(g, x, y);
                File source = new File(path);  
                long lastModified = source.lastModified();
                String time = format(lastModified);
                y++;
                double size=file.length();
                String s=Double.toString(size)+" bytes";
                jTable1.getModel().setValueAt(s, x, y);
                y++;
                jTable1.getModel().setValueAt(time, x, y);
                y++;
                jTable1.getModel().setValueAt(h,x,y);
                y=0;
                x++; 
                }
                
                
            }
            
           
        }
    }
    

    
